#include "main.h"
/**
* more_numbers - print numbers from 0 to 14
* Return: return 10 times numbers from 0 to 14
*/
ivoid more_numbers(void)
{
	int i, j;

	for (i = 0; i < 10; i++)
	{
		for (j = 0; j <= 14; j++)
		{
			if (j > 9)
			{
				_putchar((j / 10) + '0');
			}
			_putchar((j % 10) + '0');
		}
		_putchar('\n');
	}
}
